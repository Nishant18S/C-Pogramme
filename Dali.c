#include <stdio.h>
#include <string.h> // Include string.h for string functions

int main() {
    char c[50];
    
    printf("Enter your Girlfriend's Name: ");
    scanf("%s", c); // Read a string (name) from the user
    
    // Compare the input string with "Aliva"
    if (strcmp(c, "Aliva") == 0) {
        printf("Yes, %s is your girlfriend.\n", c);
    } else {
        printf("No\n");
    }
    
    return 0;
}





